# Remove lat, long, birthyear, and gender fields as this data was dropped beginning in 2020 and we assume 2020 is the format moving forward
all_trips <- all_trips %>%  
  select(-c(start_lat, start_lng, end_lat, end_lng, birthyear, gender, "01 - Rental Details Duration In Seconds Uncapped", "05 - Member Details Member Birthday Year", "Member Gender", "tripduration"))

#========================================================
#Step 3: Cleaning and adding data to prepare for analysis
#========================================================
# Inspecting the new combined data frame
colnames(all_trips)  #List of column names
nrow(all_trips)  #Number of rows
dim(all_trips)  #Overall dimensions of the data frame (columns and rows)
head(all_trips)  #Seeing the first 6 rows of the data frame.  
